package sample;

public class Variable {

    public static void main(String[] args) {
        var number1 = 1;
        System.out.println(number1);
        
        String str1;
        str1 = "abc";
        System.out.println(str1);
        
        // printlnで出力（出力後改行を行う）
        System.out.println(str1);
        System.out.println(str1);
        
        // printで出力（改行を行わない）
        System.out.print(str1);
        System.out.print(str1);
        System.out.print(str1);

        
    }

}
